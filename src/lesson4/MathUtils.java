package lesson4;

public class MathUtils {

    public static int add(int a, int b) {
        return a + b;
    }

    public static int subtract(int a, int b) {
        return a - b;
    }

    public static boolean IsEven(int a) {
        boolean e;

        if ((a % 2) == 0)
            return e = true;
        else
            return e = false;
    }

}
